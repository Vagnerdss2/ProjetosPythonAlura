import random

def jogar():
    print('*' * 60)
    print('JOGO DA FORCA'.center(60))
    print('*' * 60)

    arquivo = open('palavra.txt', 'r')
    palavras = []

    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()

    numero = random.randrange(0,len(palavras))
    palavra_secreta = palavras[numero].upper()

    
    chute_guardado = ['_' for letra in palavra_secreta]
    

    acertou = False
    enforcou = False
    erro = 0

    print(chute_guardado)

    while not acertou and not enforcou:
        chute = input('Chute uma letra: '.lower().strip()).upper()
        
        posicao = 0
        
        if chute in palavra_secreta:
            for letras in palavra_secreta:
                if letras == chute:
                    chute_guardado[posicao] = letras
                posicao += 1
        else:
            erro +=1
            desenha_forca(erro)
            print(f'Erro {erro} de 7')
        enforcou = erro == 7
        acertou = '_' not in chute_guardado 
        print(chute_guardado)

    if acertou:
        imprime_mensagem_vencedor()
    else:
        # print('Você perdeu!!')
        imprime_mensagem_perdedor(palavra_secreta)

        print(chute_guardado)
       

